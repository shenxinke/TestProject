package com.yst.onecity.bean;

/**
 * 只有code和message的实体类
 *
 * @author lxiangchao
 * @version 3.2.1
 * @date 2017/9/21.
 */
public class CodeMsgBean {

    private int code;
    private String msg;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
